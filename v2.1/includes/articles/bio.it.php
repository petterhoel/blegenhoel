<p class="lead-in">Øystein Blegen Hoel (n. 1948) lavora e abita a Oslo, in Norvegia. Ha un piccolo studio, Il Bosco del Merlo, situato appunto in un boschetto insieme a merli e ad altri uccelli e a qualche alce.</p>

<section class="body-text">
    <img src="img/bio-dummy.jpg">
    <p>Le sue opere sono eseguite con diverse tecniche: acquarello, guazzo, inchiostro di china, carbone acrilico e olio. Dipinge intuitivamente. Uccelli piccoli e grandi, animali e creature fantastiche sono temi che si ripetono nelle sue opere. Egli prende ispirazione dalla natura, soprattutto dalle formazioni e dai processi che non sono del tutto evidenti. Anche le emozioni umane e la subcoscienza sono per lui parte del concetto di natura. <span class="nobr">– Sono</span> più interessato ad esprimere l’interiorità che l’aspetto esteriore delle cose. Quello che si può anche chiamare l’ energia dietro la facciata, dice lui, anche quell’energia che risveglia l’umorismo.
    </p>

    <p>Øystein Blegen Hoel ha lavorato per più di trent’ anni come creatore e consulente strategico nel campo pubblicitario. Ha ricevuto diversi premi sia nazionali che internazionali in questo ambito. Ha studiato marketing, giornalismo e comunicazione visiva.
    </p>

    <p>Il suo interesse originario per la pittura e per il disegno l’ha portato ora a seguire il suo desiderio di ritornare al pennello e al pennino, desiderio che è maturato fino a diventare una sfida. A partire dal 2000 la sua attenzione si è rivolta soprattutto a lavorare e a pensare in maniera visuale. In questo senso sono stati un’importante fonte di ispirazione i consigli, gli scambi di idee e l’incoraggiamento di pittori noti e di altre persone competenti in materia. Così come sono stati importanti i viaggi di studio e di ispirazione in luoghi come la Danimarca, l’Olanda e l’ Italia.
    </p>

