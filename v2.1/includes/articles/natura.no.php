	<h2>Hull i gjerdet</h2>

	<p>Veien går igjen til Østmarka. Men ikke lenger inn enn til Nøklevann. Dette vannet som var en del av Oslos drikkevannsforsyning tilbake til 1903. På et tidspunkt ble det i god avstand fra vannet satt opp et høyt og for de fleste ugjennomtrengelig gjerde. 
	</p>

	<p>Fra 1983 kom vannet i springen annetsteds fra og Nøklevann ble tilgjengelig for bading, fisking, padling, grilling, leik og alt annet det er mulig å finne på ved et vann i marka.
	</p>

	<quote>“ . . ble rett og slett forført av denne åpningen . . ”</quote>

	<p>Det må ha vært en sensommer et år eller to i forkant av dette jeg oppdaget en åpning i gjerdet et sted i nærheten av gamlebakken opp mot Sarabråten. Jeg ble rett og slett forført av denne åpningen, og før jeg visste ordet av det var jeg geleidet inn i et skogsområde som jeg aldri hadde sett maken til. Her lå det gamle, grå trær, røtter og kvister hulter i bulter, bukket under av alder eller en høststorm. Mye av dette og annen vegetasjon var delvis dekket av mose og lav med og uten skjeggvekst. Også deler av skogbunnen var teppelagt med tykk mose, som fikk meg til å føle at den ikke skulle tråkkes på. I hvert fall ikke av skapninger som meg. Sopp i ymse størrelser og fasonger befant seg både på bakken og oppetter trestammer. Ved rennende vann og der lyset så vidt hadde trengt seg ned var det tydelige tegn til ny vekst.  
	</p>

	<quote>“ . . brillene ble til mikroskop.”</quote>

	<p>Der og da framsto det ganske åpenbart at er det steder man ikke skal overse detaljene, så er det i naturen. Spesielt ikke i gammel skog. Det var som om brillene ble til mikroskop. Materie, form, farge og bevegelser ble forstørret og forsterket til svært underlige, men samtidig helt fantastiske former og komposisjoner. Blandingen av luft og lukt lignet ikke på noe jeg tidligere hadde kjent. Ikke var det parfyme, ikke var det gammalost, men så absolutt en helt spesiell eim av skog som hadde levd et lang liv, kom jeg omsider fram til. Og i dette skulpturelle landskapet snek biller og andre insekter seg inn i de lenge levde trestammers irrganger. Der inne skulle de gjøre nytte for seg og sine omgivelser, så sant de ikke tok seg så god tid at nebbet til en sulten fugl kom dem i forkjøpet.
	</p>

	<p>Jeg vet ikke hvor lenge jeg befant meg i dette landskapet. Men jeg husker at jeg følte meg både mett og utmattet – og ikke så lite lykkelig der jeg vandret tilbake til den vanlige virkelighten.
	</p>

	<img src="img/natura-text-4.jpg">

	<p>Jeg kunne nevnt mange eksempler på inntrykk og opplevelser som naturen har bydd på. Også de som har fremkalt frykt og redsel. Jeg kunne snakket om naturens storhet, dens voldsomme krefter, uforutsigbarhet og skjørhet. Om ressurser, vern, vårt misbruk, myter og overtro. Og selvsagt om det faktum at vi mennesker er en del av den, enten vi har glemt det eller ikke. Men jeg har valgt de hendelsene du nå har fått et lite innblikk i, ikke bare fordi de er spesielle og uforglemmelige for meg, men fordi de ligger så langt tilbake tid at jeg med sikkerhet kan si at de har bidratt til å utvikle min fantasi og indre billedverden. At de har skapt ringvirkninger som har vært med på å forme en del av min måte å tenke på. Og hva annet kan man male, enten assosiasjonene er natur eller noe annet, enn tolkninger av egne tanker og impulser.
	</p>

	<p>Siden årtusenskiftet har jeg igjen vært så heldig å bo og arbeide et sted der byen møter skogen. Avslutningsvis noen linjer jeg skrev ned etter en tur i mine nære omgivelser:
	</p>
</section>

