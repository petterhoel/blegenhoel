	<h2>Caffelatten fra Trysil</h2>

	<p>Det meste av min oppvekst fant sted der Østmarka begynte, før ordet drabantby var innlemmet i vokabularet. Der hvor det lå noen hus i kanten av Oslos utmark, ved skogen som fremdeles strekker seg til svenskegrensen og vel så det. Dette var min lekeplass og tumleplass. Her utspant det seg oppdagelsesferder, kriger, mageplask og andre plask i vann som ikke var større enn at de alltid var speilblanke. Og ikke å forglemme selvoppgåtte skispor på breie løypeski – og djerve svev med de samme skiene.
	</p> 

	<quote>“ .. og djerve svev med de samme skiene.”</quote>

	<p>En opplevelse og erfaring jeg nå føler er ganske eksklusiv er at jeg vinteren som seksåring fikk det det privilegium å være skogsarbeiderenes maskot. Her skulle det ryddes plass til vertikaltdelte trehus og boligblokker i ymse utforminger. Skogen ble fraktet vekk med hest og slede, med meg som hyppig benyttet kusk. Det var ikke få kubikk trevirke sterke, snille, tålmodige Brunen og jeg hadde på lasset – til plassen der Bedforder med lange motorkasser og sjåfører og sidemenn med brede brøstkasser erstattet strie elvestryk og tømmerfløtere. De sa så, mine arbeidskamerater.
	</p> 

	<p>Som ganske fast inventar i spisepausene på brakka burde jeg muligens blitt utropt til landets første caffelatte-drikker. Mye latte i kaffen til guttungen, vel å merke. Joda, de varmet opp latten, karene fra de enda dypere skoger enn Østmarka. Det var jo kaldere om vinteren før. Når kvelden nærmet seg var det ofte jeg som fikk lose Brunen til fortjent foring og stell i stallen på småbruket Bråten på Trasop. Det vil si, veien fant han nok selv. Det var jo der han bodde. Om jeg var under oppsyn? For det meste, får jeg håpe. En bonus i ettertid er at jeg har fått en nærmest utenomnaturlig evne til å hente fram eiemen av nyhogd tømmer og kvae.
	</p>

	<img src="img/natura-text-2.jpg">

	<h2>Gutta på skauen</h2>

	<p>Langt inne i Østmarka ligger det noen enkle, små og godt gjemte koier. De ble brukt som skjule- og hvilesteder for folk som var på flukt til Sverige under krigen. Så godt gjemt er de. Noen er utstyrt med et sprikende plankegolv, andre med jordgolv. Noen med en liten vedovn og piperør, andre med et røykhull i taket. Ellers var de kun utstyrt med brisker, best egnet for dem som foretrekker hard madrass og pinnestoler.
	</p>

	<quote>“ . . en flukt med geværer i hælene.”</quote>

	<p>Fra midt i tenårene var vi fire–fem i kameratflokken som hadde disse koiene som fristed, takket være eldstemann Lasse som hadde fått vite om hemmelighetene fra sine enda eldre bekjente som hadde klart den bragden det var å komme seg velberget over grensen. På grunn av koienes plassering i terrenget, og spesielt etter mørkets frembrudd, var det umulig å ikke prøve på noe så umulig som å leve seg inn i en flukt med geværer i hælene.
	</p>

	<p>Men ingenting kan overskygge at det først og fremst er følelsen av frihet og ekte skog som sitter igjen fra disse områdene som var vår hemmelige tumleplass vekselvis vår,  høst og vinter i løpet av en fireårsperiode. Vi tilbrakte påske og pinse og feiret både 17. mai og nyttårsaften i disse nær uberørte omgivelsene. Ja, bildene er klare. Skiturene innover med bytte til truger før forsering av de bratteste åsene. Lukt av årstider. Lys fra stjerner på en nattehimmel som fikk oss til å nevne de tre vise menn. Et dyr som i noen sekunder sto der stille som en skulptur og været for så på et blunk returnere til sin kamuflerte tilværelse. Pluss de dyrene som befant seg like i nærheten, men ikke etterlot seg andre spor enn et kadaver eller hårete ekskrementer. Kun én gang  opplevde vi å se folk, ikke langt fra en koie som lå på den andre siden av det vannet jeg ikke vil huske navnet på. Dagen etter var det ingen å se. 
	</p>

	<p>Natterstider kom vi ikke utenom tanker om at skogen også inneholder menneskelige skjebner, som må beskrives som alt annet enn idyll. Natur det også, kan man si.
	</p>
