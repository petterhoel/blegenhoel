
<h1>Natura +</h1>
<p class="lead-in">Jeg maler fordi jeg har glede av det. En glede som for alvor begynte i relativ sen alder, da tiden var inne for å legge andre gjøremål til side. Med andre ord er jeg ganske ung som maler.</p>

<section class="body-text">

	<p>Jeg maler ikke alltid natur, men jeg tenker ofte på natur når jeg maler. 
	Spesielt skog. Hav og fjell også. Og den menneskelig natur som en del av et hele. Men som oftest er det assosiasjoner til skog som dukker opp. Disse tankene kommer av seg selv uten at jeg egentlig har gjort noe for det, har jeg tenkt når jeg først begynte å tenke over det.
	</p>

	<p>Slik er det ikke.</p>

