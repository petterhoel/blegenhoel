<p class="lead-in">Øystein Blegen Hoel (f 1948) bor og arbeider i Oslo. Han holder til i Studio il Bosco del Merlo, et lite studio i en liten skog, sammen med svarttroster og andre fugler, samt en og annen elg.</p>

<section class="body-text">
	<img src="img/bio-dummy.jpg">
	<p>Han arbeider med forskjellige medier; akvarell, gouache, tusj, kull, akryl og olje. Han maler intuitivt. Små og store fugler, dyr og fantasivesener går ofte igjen i motivene. Inspirasjon henter han fra naturen, særlig formasjoner og prosesser som ikke er helt åpenbare. Menneskelige følelser og underbevissthet er også med i naturbegrepet.  <span class="nobr">– Jeg</span> er mer opptatt av å uttrykke innsiden enn utsiden. Kall det gjerne energien bak fasaden, sier han. <span class="nobr">– Inkludert</span> humor.</p>

	<p>Øystein Blegen Hoel har i mer enn 30 år jobbet som kreatør og strategisk rådgiver i reklamebransjen. Han har mottatt en rekke nasjonale og internasjonale bransjepriser. Han har utdannelse innen markedsføring, journalistikk og visuell kommunikasjon.</p>

	<p>Med sin grunnleggende interesse for maling og tegning har imidlertid lysten til å rykke tilbake til start, til pensel og pennesplitt, modnet og blitt en utfordring. Å tenke og arbeide visuelt har siden årtusenskiftet utviklet seg til å bli hans hovedbeskjeftigelse. En lærerik og viktig inspirasjonskilde har vært veiledning og oppmuntring fra etablerte kunstnere og andre kunstfaglige personer. Likeledes studie- og inspirasjonsreiser blant annet til Danmark, Nederland og Italia.</p>

