	<h2>Il caffelatte di Trysil</h2>

	<p>La maggior parte della mia infanzia si è svolta là dove Østmarka inizia, prima che la parola sobborgo fosse inserita nel vocabolario. La dove c’ erano alcune case vicino alla forresta di Oslo presso la forresta che ancora si estende fino al confine svedese e oltre. Questo era il mio parco giochi e di incontri. Qui si svolgevano scoperte, guerre, salti in acqua e altri salti che non erano più grandi e che erano sempre chiari. Da non dimenticare piste da sci – e corraggiosi voli con gli stessi sci.
	</p> 

	<quote>“ .. og djerve svev med de samme skiene.”</quote>

	<p>Un sperienza che sento ora essere piutusto esclusiva era avere durante l’ inverno in cui avevo sei anni, il privilegio di essere la mascot dei lavoratori forrsetali. Qui si doveva fare spazio per case di legno divise verticalmente e per palazzi di diverse forme. La forresta è stata portata via da cavalli e slitte con me che spesso veniva utilizzato come fantino. Non era no pochi metri cubi di legno che io e il forte, buono e paziente Bruno avevamo come carico – nel posto dove le Bedford con le lunghe casse del motore e con l’ autista e l’ auitante con larghi petti sostitucivano le forti correnti dei fiume e i zatterieri. Dicevano così, i miei colleghi di lavoro.
	</p> 

	<p>Come uno dei partecipanti fissi della pausa pranzo nella baracca, probabilmente sarò stato nominato il primo bevitore di caffelatte della Norvegia. Molto latte nel caffè del bambino, ricordati. Si, hanno scaldato il latte i ragazzi della forresta ancora più profonda di quella di Østmarka. Era ancora più freddo l’ inverno di allora. Quando la sera si avvicinava ero spesso io che accompagnavo Bruno al pasto e alle cure che si era guadagnato nella stalle della fattoria Trasop. Si puo dire, che la strada la trovava da solo. Era li che abitava. Ero tennuto d’ occhio? Per lo più, spero. Un bonus per il futuro era che ho avuto  un’ abilità quasi innaturale di sentire il profumo di trochi appena tagliati e di resina.
	</p>

