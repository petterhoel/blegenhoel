	<h2>Il buco nello steccato</h2>

	<p>La strada porta di nuovo a Østmarka. Ma non oltre che per Nøklevann. Questo lago era una parte del rifornimento dell’ acqua potabile di Oslo nel 1903. A un certo punto venne, abbastanza distante dal lago, alzata,  una rete che per la maggior parte delle persone era insuperabile.
	</p>

	<p>Dal 1983 l’ acqua dei rubinetti venne da altri posti e Nølevann divenne una zona della forresta adatta alla balneazione, alla pesca, alla rema con pagaia, alla cucina alla griglia, al gioco e alle altre cose che si possono fare in un lago nella forresta.</p>

	<quote>“ . . ble rett og slett forført av denne åpningen . . ”</quote>

	<p>Una tarda estate, un anno o due prima che io mi accorgessi di un’ apertura nella rete vicino alla vecchia salita verso Sarabråten, sono stato proprio sedotto da questa appertura, e prima che io sapessi la parola di questa cosa sono stato transportato in una parte della forresta che non avevo mai visto prima. Li c’ erano vecchi alberi grigi, radici e rami qui e lá, distrutti dall’ età e dalla tempesta autunnale. Molta di questa e altra vegetazione era in parte coperta da muschio e licheni con o senza barba. Anche grandi parti del fondo della forresta erano ricoperti di spesso muschio, che mi facevano pensare che non si dovessero calpestare. Almeno non da esseri come me. Funghi di diverse  dimensioni e forme si trovano sia per terra che lungo i tronchi. Dove l’ acqua scorreva e dove la luce, appena entrava, mostrava chiari segni di nuova vegetazione.
	</p>

	<quote>“ . . gli occhiali diventano un microscopio.”</quote>

	<p>A quel tempo era piuttosto chiaro che questo era un posto dove non si dovevano trascurare i dettagli, è così in natura. Specialmente nelle vecchie forreste. Era come se gli occhiali diventassero microscopi. Le materie, le forme, i colori e i movimenti vennero ingranditi e rafforzati in modo strano, ma contemporaneamente con forme e composizioni fantastiche. Il miscuglio di aria e profumo non assomiglia va a niente che connocevo da prima. Non era profumo, non era vecchio formaggio, ma assolutamente uno speciale profumo di forresta che aveva vissuto una lunga vita, ciò era quello che mi sembrava. In questo paessagio scultoreo c’ erano coleotteri e altri insetti nei tronchi che avevano vissuto a lungo nei tunnel scavati. Lì dentro si dovevano rendere utili nel loro abitat, ma non passava molto tempo prima che il becco di un uccello affamato li prendesse.
	</p>

	<p>Non so quanto tempo sono stato in questo paesaggio. Ma mi ricordo che mi sentivo sia sazio che stremato – e molto felice quando ritornai alla realità normale.
	</p>

	<img src="img/natura-text-4.jpg">

	<p>Potrei nominare molti esempi dell’ effetto e dell’ asperienza che la natura mi ha dato. Anche quelle che hanno creato paura e terrore. Potrei parlare della grandezza della natura,  delle sue forze enormi, dell’ imprevedibilità e della bellezza. A proposito delle risorse, protezioni, miti e credenze. Ovviamente il fatto che noi umani siamo una parte di questo, sia che lo abbiamo dimenticato o no. Non ho scelto questi avvenimenti, solo perchè sono speciali e indimenticabili per me, ma perchè sono un periodo così lontano che posso dire con certezza che sono stati importanti per svilluppare la mia fantasia e il mio mondo figurativo interiore immaginativo. Si può dire che sono stati presenti nel formare una parte importante del mio modo di pensare. Che cosa altro si può dipingere, sia quando l’ associazione è la natura, o qualcosa d’ altro, che i propri pensieri gli impulsi.
	</p>

	<p>Dal nuovo millenio sono stato nuovamente così fortunato di abitare e anche lavorare in un posto dove la città incontra la forresta. Concludo con alcune linee che ho scritto tempo fa dopo un giro vicino a casa.
	</p>
</section>

