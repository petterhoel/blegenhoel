	<h2>I ragazzi nella forresta</h2>

	<p>Nella profonda Østmarka ci sono alcune semplici, piccole e ben noscoste baite. Sono state usate come nascondigli e luoghi di riposo per la gente che scappava in Svezia durante la seconda guerra mondiale. Sono molto bene nascoste. Alcune hanno un pavimento con assi non unite, altre con pavimento di terra. Alcune hanno una piccola stufa a legna con camino, altre hanno un buco per l’ uscita del fumo dal tetto. Altrimenti avevano dei sedili, adatti solo per chi preferice materassi molto duri e sedie di legno.
	</p>

	<quote>“ . . en flukt med geværer i hælene.”</quote>

	<p>A metà della gioventù eravamo quattro o cinque del gruppo di amici che avevano queste baite come posti per il tempo libero, grazie al ragazzo maggiore Lasse che aveva saputo i segreti dai suoi connocenti più vecchi che erano riusciti nell’ impresa di attraversare sani e salvi il confine.
	</p>

	<p>A causa della posizione delle baite nel terreno, e specialmente dopo l’ arrivo del buio, era impossibile non provare qualcosa di così impossibile come provare a vivere l’ esperienza di scappare con fucili addosso.
	</p>

	<p>Ma nessuno può nascondere che la sensazione di libertà e di vera forresta resta da queste zone che erano i nostri luoghi di gioco segreti sia in primavera, che in autunno e inverno durante un periodo di quattro anni. Ci abbiamo trascorso pasqua e pentecoste e festeggiato sia il 17 maggio che capodanno. In queste zone quasi intoccate. Si, le immagini sono chiare. I giri con gli sci con il cambio delle racchette da neve prima dei passagi sulle colline più ripide – il profumo, la luce e le stelle del cièlo notturno che ci faccevano nominare i tre remagi. Animali che per qualche secondo avevano la forma come una scultura e attenti per pòi, in un attimo ritornare ai loro luogi nascosti – in oltre gli animali che si trovano nelle vicinanze, ma che non lasciavano altre tracce che un cadavere oppure escrementi pellosi. Solo una volta abbiamo visto gente – non lontano da una baita che si trovava dall’ altra parte del laghetto del quale non voglio ricordare il nome. Il giorno dopo non si vedeva nessuno.
	</p>

	<p>Bisogna ricodare i pensieri che facevamo sul fatto che la forresta contiene anche destini umani, che si devono descrivere come tutt’ altro che idillici. Anche questo è natura, si può dire. 
	</p>
