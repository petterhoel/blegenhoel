
<h1>Natura +</h1>
<p class="lead-in">Dipingo perchè mi fa piacere. Un piacere che è diventato serio relativamente tardi, nel momento in cui era ora di abbandonare altre attività. In altre parole sono abbastanza giovane come pittore.</p>

<section class="body-text">

	<p>Non dipingo sempre la natura, ma penso spesso alla natura quando dipingo. Specialmente alla foresta. Anche a mare e montagne. E alla natura umana come parte di tutto il creato. Ma spesso mi vengono le associazioni alla forresta. Questi pensieri vengono da soli senza che io abbia fatto qualcosa per averli, ho pensato quando una volta ho comminiciato a pensarci.
	</p>

	<p>Non è così.</p>

