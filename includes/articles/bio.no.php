<p class="lead-in">Øystein Blegen Hoel (f 1948) bor og arbeider i Oslo. Han holder til i Studio il Bosco del Merlo, et lite studio i en liten skog, sammen med svarttroster og andre fødte flygere, samt en og annen elg.</p>

<section class="body-text">
	<img src="img/bio-dummy.jpg">
	<p> 
    Han arbeider med forskjellige medier: olje, akryl, akvarell, gouache, tusj og kull. Han maler intuitivt. Inspirasjon henter han fra naturen, særlig fra formasjoner og prosesser som ikke er helt åpenbare. – Jeg er mer opptatt av å uttrykke innsiden enn utsiden; det man kan kalle energien bak fasaden, sier han. Menneskelig underbevissthet, våre følelser og handlinger er også med i naturbegrepet. Inkludert hvordan vi mer eller mindre bevisst og ubevisst er med på å bringe våre omgivelser i harmoni og disharmoni. 
	</p>

	<p>Øystein Blegen Hoel har i mer enn 30 år jobbet som kreatør og strategisk rådgiver i reklamebransjen. Han har mottatt en rekke nasjonale og internasjonale bransjepriser. Han har utdannelse innen markedsføring, journalistikk og visuell kommunikasjon. Tidligere har han betraktet sine kunstfaglige studier som en form for selvrealisering. Men nå, som studeringen har vist seg å vedvare i flere tiår, er den muligens å regne som en utdannelse, hvis man ser bort fra det formelle.</p>

	<p>Med sin grunnleggende interesse for maling og tegning har imidlertid lysten til å rykke tilbake til start, til pensel og pennesplitt, modnet og blitt en utfordring. Å tenke og arbeide visuelt har siden årtusenskiftet utviklet seg til å bli hans hovedbeskjeftigelse. En lærerik og viktig inspirasjonskilde har vært veiledning og oppmuntring fra etablerte kunstnere og andre fagpersoner. Likeledes studie- og inspirasjonsreiser til blant annet Danmark, Nederland, Spania og Italia.</p>

