<h1>Natura +</h1>
<p class="lead-in">Dipingo perchè mi piace. E’ questo un piacere diventato serio relativamente tardi, nel momento in cui era ora di abbandonare altre attività. In altre parole sono abbastanza giovane come pittore.</p>

<section class="body-text">

	<p>Non dipingo sempre la natura, ma penso spesso alla natura mentre dipingo. Specialmente al bosco. Anche al mare e alle montagne. E alla natura umana come parte del tutto. Ma spesso mi vengono in mente associazioni al bosco. Questi pensieri arrivano da soli, senza che io abbia fatto nulla – penso, subito dopo aver cominciato a pensare.
	</p>

	<p>Non è così.</p>

	<div id="about-1" class="scroll-over-image"></div>

	<h2>Il caffelatte di Trysil</h2>

	<p>La maggior parte della mia infanzia si è svolta là dove inizia il bosco orientale di Østmarka, prima che la parola sobborgo fosse inserita nel vocabolario. Là dove c’erano alcune case sul limitare di Oslo, all’ombra dei primi alberi del bosco che ancora si estende fino al confine svedese e oltre. Questo era il mio parco giochi e il mio spazio libero. Qui avvenivano esplorazioni, guerre, tuffi di pancia ed altri tuffi in un lago piccolo e dalle acque così tranquille che ci si poteva specchiare. Per non scordare i fuoripista sugli sci e i salti arditi - sempre sugli stessi sci.
	</p> 

	<quote>“ . . e salti arditi sempre sugli stessi sci.”</quote>

	<p>All’età di sei anni ho vissuto quella che credo sia stata un’esperienza quasi unica: essere la mascotte dei lavoratori forestali per un intero inverno. Si doveva fare spazio a delle case di legno bifamiliari e a diversi palazzi. Il bosco è stato trasportato via da cavalli e slitte con me che spesso facevo da fantino. Non erano pochi i metri cubi di legno che io e il forte, buono e paziente Bruno avevamo come carico – fino al posto dove i camion Bedford dal motore allungato e gli autisti e gli aiutanti dal largo torace sostituivano le forti correnti del fiume e gli zatterieri, così come dicevano i miei colleghi di lavoro.
	</p> 

	<p>In quanto uno dei partecipanti fissi alla pausa pranzo nella baracca, probabilmente sono stato acclamato il primo bevitore di caffelatte della Norvegia (il caffelatte non esisteva ancora in Norvegia a quel tempo). Molto latte nel caffè del bambino, ricordati. Sì, hanno scaldato il latte i ragazzi della foresta - si trattava di uomini che venivano da una foresta più fitta del nostro bosco orientale di Østmarka. Allora era più freddo l’inverno. Quando si avvicinava la sera ero spesso io ad accompagnare Bruno alle stalle della fattoria Bråten a Trasop, al pasto e alle cure che si era guadagnato. In realtà, la strada si può dire che la trovasse da solo. Era lì che abitava. Ero tenuto d’occhio? Per lo più, spero. Un regalo per il futuro è stato quello di avere acquisito un’abilità quasi fuori dal naturale di sentire il profumo di tronchi appena tagliati e di resina.
	</p>

  <div id="about-2" class="scroll-over-image"></div>

	<h2>I ragazzi nel bosco</h2>

	<p>Nel profondo bosco orientale di Østmarka ci sono alcune semplici baite, piccole e ben nascoste. Sono state usate come nascondigli e luoghi di riposo dalla gente che scappava in Svezia durante la seconda guerra mondiale. Sono molto ben nascoste. Alcune hanno un pavimento con delle assi non unite tra loro, altre hanno il pavimento di terra. Alcune hanno una piccola stufa a legna con camino, altre hanno un buco per l’uscita del fumo dal tetto. Per il resto c’erano soltanto delle panche, adatte solo a chi piacciono materassi molto duri, e delle sedie di legno.
	</p>

	<quote>“ . . scappare con dei fucili alle calcagna.”</quote>

	<p>A partire dalla mia adolescenza eravamo in quattro o cinque ragazzi ad usare queste baite come luoghi di evasione in cui tutto era possibile. Era stato Lasse, il maggiore tra noi, a portarci lì la prima volta, dopo averne saputo l’esistenza, in segreto, da alcune persone che erano riuscite nell’impresa di attraversare sane e salve il confine con la Svezia.
	</p>

	<p>A causa della dislocazione delle baite, e specialmente dopo l’arrivo del buio, era impossibile non provare qualcosa di così impossibile come immaginare di scappare con dei fucili alle calcagna.
	</p>

	<p>Ma nulla può nascondere che ciò che resta di questo periodo di quattro inverni, primavere, estati e autunni, è il sentimento di libertà la sensazione di essere nel vero bosco che era il nostro luogo segreto di giochi. In queste zone quasi inesplorate abbiamo trascorso Pasque e Pentecosti, festeggiato sia la festa nazionale, ogni 17 maggio, che i capodanni. Sì, le immagini sono chiare. I giri con gli sci e con le ciaspole per i passaggi sui pendii più ripidi. Il profumo delle diverse stagioni. La luce delle stelle nel cielo notturno che ci faceva pensare ai tre Re magi. Un animale che per qualche secondo se ne stava immobile come una forma scolpita, in ascolto, per poi, in un attimo, ritornare alla sua vita mimetica. Oltre agli altri animali che si trovavano nelle vicinanze, ma che non lasciavano altre tracce che un cadavere o degli escrementi pelosi. Solo una volta abbiamo visto della gente – non lontano da una baita che si trovava dall’altra parte del laghetto di cui non voglio ricordare il nome. Il giorno dopo non si vedeva più nessuno.
	</p>

	<p>Durante la notte non potevamo evitare di pensare che il bosco contiene anche destini umani tutt’altro che idillici. Anche questo si può dire che sia natura. 
	</p>
  <div id="about-3" class="scroll-over-image"></div>

	<h2>Il foro nella rete di filo spinato</h2>

	<p>Il corso dei miei pensieri mi porta di nuovo al bosco orientale di Østmarka. Ma non oltre il lago di Nøklevann. Questo lago costituiva una parte del rifornimento di acqua potabile di Oslo fino al 1903. Ad un certo punto venne innalzata una rete di filo spinato ad una certa distanza dal lago. Un’alta rete che per la maggior parte delle persone era insuperabile.
	</p>

	<p>Dal 1983 l’acqua dei rubinetti proviene da altri posti e Nøklevann è tornato ad essere aperto ai bagni, alla pesca, alle remate in canoa, alle grigliate, ai giochi e a tutte le altre cose che si possono fare in un lago nel bosco.</p>

	<quote>“Rimasi completamente sedotto da questa apertura . . ”</quote>

	<p>Uno o due anni prima di questo evento, in tarda estate, avevo scoperto un’apertura nella rete in un posto vicino ad una vecchia salita verso Sarabråten. Rimasi completamente sedotto da questa apertura, e ancor prima di rendermene conto, mi trovai trasportato in una parte del bosco che non avevo mai visto prima. Là c’erano vecchi alberi grigi, radici e rami un po’ dappertutto, distrutti dall’età o da qualche tempesta autunnale. Molta della vegetazione era in parte coperta da muschio e licheni con o senza barba. Anche il terreno sotto al bosco era per lo più ricoperto di spesso muschio, cosa che mi faceva pensare che non dovesse essere calpestato. Almeno non da esseri come me. Funghi di diverse dimensioni e forme si trovavano sia per terra che sui tronchi. Nei punti in cui l’acqua scorreva e dove la luce riusciva a penetrare si scorgevano chiari segni di nuova vegetazione.
	</p>

	<quote>“ . . come se i miei occhiali fossero diventati microscopi.”</quote>

	<p>In quel momento mi apparve piuttosto evidente che se c’è un posto dove non si devono trascurare i dettagli, questo è proprio la natura. Specialmente nei vecchi boshi. Era come se i miei occhiali fossero diventati microscopi. Le materie, le forme, i colori e i movimenti venivano ingranditi e intensificati in figure e composizioni strane ma al contempo fantastiche. Il miscuglio di aria e profumo non assomigliava a niente che conoscessi da prima. Non era profumo, non era nemmeno odore di vecchio formaggio, ma proprio uno speciale profumo di un bosco che aveva vissuto una lunga vita - questo è quello che mi veniva in mente. In tale paessagio scultoreo, coleotteri ed altri insetti si erano infilati nei tronchi longevi attraverso dei tunnel da loro scavati. Lì dentro dovevano darsi da fare per proteggere se stessi e il loro ambiente. Ma non passava molto tempo prima che il becco di un uccello affamato li afferasse.
	</p>

	<p>Non so quanto tempo rimasi in questo paesaggio. Ma mi ricordo che mi sentivo sia sazio che stremato – e molto felice quando ritornai alla solita realtà.
	</p>

  <div id="about-4" class="scroll-over-image"></div>

	<p>Potrei raccontare molte storie sull’effetto e sulle esperienze che la natura mi ha fatto vivere. Anche quelle che mi hanno provocato paura e terrore. Potrei parlare della grandezza della natura, delle sue forze violente, dell’imprevedibilità e della fragilità. Delle risorse, delle zone protette e delle nostre violazioni, dei miti e delle credenze. E del fatto che noi umani siamo parte di questo, che lo abbiamo dimenticato o meno. 
	</p>

	<p>Ma ho scelto di raccontare proprio questi avvenimenti non solo perchè sono speciali e indimenticabili per me, ma perchè appartengono ad un periodo così lontano che posso dire con certezza che sono stati importanti per lo sviluppo della mia fantasia e del mio immaginario. Hanno avuto conseguenze che hanno contribuito a formare parte del mio modo di pensare. E cos’altro si può dipingere - che l’associazione sia la natura o qualcosa d’altro - se non le interpretazioni dei propri pensieri e dei propri impulsi?
	</p>

	<p>Dall’inizio del nuovo millenio sono stato di nuovo così fortunato da abitare e lavorare in un posto dove la città incontra il bosco.
	</p>

	<p>
	Concludo con alcune righe scritte tempo fa in seguito ad un giro nei dintorni.	
	</p>
